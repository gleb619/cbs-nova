package cbs.dsl.codegen;

import javax.annotation.processing.Filer;
import javax.tools.FileObject;
import javax.tools.JavaFileObject;
import javax.tools.StandardLocation;

import java.io.IOException;
import java.io.PrintWriter;
import java.util.List;

public class RegistrationGenerator {

  private final Filer filer;

  public RegistrationGenerator(Filer filer) {
    this.filer = filer;
  }

  public void generate(List<RegistrationSpec> registrations) throws IOException {
    generateImplRegistrations(registrations);
    generateSpiFile();
  }

  private void generateSpiFile() throws IOException {
    FileObject spiFile = filer.createResource(
        StandardLocation.CLASS_OUTPUT, "META-INF/services", "cbs.dsl.api.ImplRegistrationProvider");
    try (PrintWriter writer = new PrintWriter(spiFile.openWriter())) {
      writer.println("cbs.dsl.codegen.generated.GeneratedImplRegistrations");
    }
  }

  private void generateImplRegistrations(List<RegistrationSpec> registrations) throws IOException {
    JavaFileObject file =
        filer.createSourceFile("cbs.dsl.codegen.generated.GeneratedImplRegistrations");
    try (PrintWriter writer = new PrintWriter(file.openWriter())) {
      writer.println("package cbs.dsl.codegen.generated;");
      writer.println();
      writer.println("import cbs.dsl.api.ImplRegistrationProvider;");
      writer.println("import cbs.dsl.api.WritableRegistry;");
      for (RegistrationSpec spec : registrations) {
        writer.printf("import %s.%s;%n", spec.packageName(), spec.className());
      }
      writer.println();
      writer.println("/**");
      writer.println(" * Generated implementation registrant.");
      writer.println(" * This file is auto-generated by the annotation processor.");
      writer.println(" * Do not edit manually.");
      writer.println(" */");
      writer.println(
          "public class GeneratedImplRegistrations implements ImplRegistrationProvider {");
      writer.println();
      writer.println("    @Override");
      writer.println("    public void register(WritableRegistry registry) {");
      if (registrations.isEmpty()) {
        writer.println("        // No DSL components to register");
      } else {
        for (RegistrationSpec spec : registrations) {
          writer.printf("        registry.register(new %s());%n", spec.className());
        }
      }
      writer.println("    }");
      writer.println("}");
    }
  }
}
